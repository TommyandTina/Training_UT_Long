/***********************************************************************************************************************
* Start of function R_OSAL_IoGetDeviceList()
***********************************************************************************************************************/
e_osal_return_t R_OSAL_IoGetDeviceList(const char * p_device_type_name,
                                       char * p_buffer,
                                       size_t buffer_size,
                                       size_t *const p_num_of_byte)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    e_osal_return_t          osal_ret;
    e_osal_return_t          local_ret;
    e_osal_init_status_t     init_status;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    init_status = R_OSAL_DCFG_IoGetInitStatus();
    if (OSAL_MANAGER_STATE_INITIALIZED != init_status)
    {
        osal_ret = OSAL_RETURN_STATE;
    }
    else if ((NULL == p_device_type_name) || (NULL == p_num_of_byte))
    {
        osal_ret = OSAL_RETURN_PAR;
    }
    else if (NULL == p_buffer)
    {
        if (0U != buffer_size)
        {
            osal_ret = OSAL_RETURN_PAR;
        }
        else
        {
            osal_ret = OSAL_RETURN_OK_RQST;
        }
    }
    else
    {
        osal_ret = OSAL_RETURN_OK;
    }   /* end of if (OSAL_MANAGER_STATE_INITIALIZED != init_status) */

    if ((OSAL_RETURN_OK == osal_ret) || (OSAL_RETURN_OK_RQST == osal_ret))
    {
        local_ret = R_OSAL_DCFG_IoGetDeviceList(p_device_type_name, p_buffer, buffer_size, p_num_of_byte);
        if (OSAL_RETURN_OK != local_ret)
        {
            osal_ret = local_ret;
        }
        else
        {
            /* Do nothing */
        }
    }
    else
    {
        /* Do nothing */
    }

    return osal_ret;
}
