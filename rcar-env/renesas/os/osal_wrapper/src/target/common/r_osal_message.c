/***********************************************************************************************************************
* Start of function R_OSAL_MqCreate()
***********************************************************************************************************************/
e_osal_return_t R_OSAL_MqCreate(const st_osal_mq_config_t *const p_config,
                                osal_mq_id_t mq_Id,
                                osal_mq_handle_t *const p_handle)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    e_osal_return_t         osal_ret;
    e_osal_init_status_t    init_status;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    init_status = R_OSAL_RCFG_MqGetInitStatus();
    if (OSAL_MANAGER_STATE_INITIALIZED != init_status)
    {
        osal_ret = OSAL_RETURN_STATE;
    }
    else if ((NULL == p_config) || (NULL == p_handle))
    {
        osal_ret = OSAL_RETURN_PAR;
    }
    else if ((0U == p_config->max_num_msg) || (0U == p_config->msg_size))
    {
        osal_ret = OSAL_RETURN_PAR;
    }
    else
    {
        osal_ret = R_OSAL_RCFG_MqCreate(p_config, mq_Id, p_handle);
    }

    if (OSAL_RETURN_OK != osal_ret)
    {
        if ((OSAL_RETURN_BUSY != osal_ret) && (NULL != p_handle))
        {
            *p_handle = OSAL_MQ_HANDLE_INVALID;
        }
        else
        {
            /* Do nothing */
        }
    }
    else
    {
        /* Do nothing */
    }

    return osal_ret;
}
