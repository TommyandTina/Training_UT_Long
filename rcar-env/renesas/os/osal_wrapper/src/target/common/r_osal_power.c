/***********************************************************************************************************************
* Start of function R_OSAL_PmWaitForState()
***********************************************************************************************************************/
e_osal_return_t R_OSAL_PmWaitForState(osal_device_handle_t handle,
                                      e_osal_pm_state_t state,
                                      osal_milli_sec_t time_period)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    e_osal_return_t         osal_ret;
    e_osal_init_status_t    init_status;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    init_status = R_OSAL_DCFG_PmGetInitStatus();
    if (OSAL_MANAGER_STATE_INITIALIZED != init_status)
    {
        osal_ret = OSAL_RETURN_STATE;
    }
    else if (OSAL_DEVICE_HANDLE_INVALID == handle)
    {
        osal_ret = OSAL_RETURN_HANDLE;
    }
    else if (0 > time_period)
    {
        osal_ret = OSAL_RETURN_PAR;
    }
    else if ((OSAL_PM_STATE_PG != state) &&
             (OSAL_PM_STATE_CG != state) &&
             (OSAL_PM_STATE_ENABLED != state) &&
             (OSAL_PM_STATE_READY != state) &&
             (OSAL_PM_STATE_RESET != state))
    {
        osal_ret = OSAL_RETURN_PAR;
    }
    else
    {
        osal_ret = R_OSAL_DCFG_PmWaitForState(handle, state, time_period);
    }   /* end of if (OSAL_MANAGER_STATE_INITIALIZED != init_status) */

    return osal_ret;
}
/***********************************************************************************************************************
* End of function R_OSAL_PmWaitForState()
***********************************************************************************************************************/
