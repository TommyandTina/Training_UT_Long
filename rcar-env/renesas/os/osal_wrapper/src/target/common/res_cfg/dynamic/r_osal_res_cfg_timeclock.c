/***********************************************************************************************************************
* Start of function R_OSAL_RCFG_ClockTimeControlInit()
***********************************************************************************************************************/
e_osal_return_t R_OSAL_RCFG_ClockTimeControlInit(void)
{
    /* Do nothing on Linux */
    return OSAL_RETURN_OK;
}
/***********************************************************************************************************************
* End of function R_OSAL_RCFG_ClockTimeControlInit()
***********************************************************************************************************************/
