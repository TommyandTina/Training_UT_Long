/***********************************************************************************************************************
 * XOS3_OSAL_LINUX_CD_CD_CLK_004
***********************************************************************************************************************/
/***********************************************************************************************************************
* Start of function R_OSAL_RCFG_ClockTimeGetTimeStamp()
***********************************************************************************************************************/
e_osal_return_t R_OSAL_RCFG_ClockTimeGetTimeStamp(e_osal_clock_type_t clock_type, st_osal_time_t* const p_time_stamp)
{
    e_osal_return_t osal_ret;

    /* clock_type does not use on Linux */
    (void)clock_type;

    osal_ret = R_OSAL_OS_ClockTimeGetHighPrecisTimeStamp(p_time_stamp);

    return osal_ret;
}
