/***********************************************************************************************************************
* Start of function R_OSAL_InterruptCheckBusy()
***********************************************************************************************************************/
e_osal_return_t R_OSAL_InterruptCheckBusy(void)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    e_osal_return_t         osal_ret;
    e_osal_init_status_t    init_status;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    init_status = R_OSAL_DCFG_InterruptGetInitStatus();
    if (OSAL_MANAGER_STATE_INITIALIZED != init_status)
    {
        osal_ret = OSAL_RETURN_STATE;
    }
    else
    {
        R_OSAL_DCFG_InterruptSetInitStatus(OSAL_MANAGER_STATE_UNINITIALIZED);
        osal_ret = R_OSAL_DCFG_InterruptCheckBusy();
        if (OSAL_RETURN_BUSY == osal_ret)
        {
            R_OSAL_DCFG_InterruptSetInitStatus(OSAL_MANAGER_STATE_INITIALIZED);
        }
        else
        {
            /* Do nothing */
        }
    }

    return osal_ret;
}
