/* PRQA S 9106 1 # This function is intended to be used in common. */
size_t R_OSAL_OS_Strnlen(const char* p_str,
                         size_t maxlen)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    size_t ret_size = 0;
    const char * local_str = p_str;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    if (NULL == local_str)
    {
        ret_size = 0;
    }
    else
    {
        /* Continue the loop until the null character is found to count up the length of p_str.
           p_str must be null-terminated. */
        while (('\0' != *local_str) && (ret_size < maxlen))
        {
            local_str++;
            ret_size++;
        }
    }

    return ret_size;
}
