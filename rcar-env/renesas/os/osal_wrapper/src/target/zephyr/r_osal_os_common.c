/* PRQA S 9106 1 # This function is intended to be used in common. */
void R_OSAL_OS_Free(void *p_ptr)
{
    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    if (NULL == p_ptr)
    {
        /* Do nothing */
    }
    else
    {
        /* PRQA S 5118 1 # Memory deallocation functions are available during deinitialization. */
        free(p_ptr);
    }

    return;
}
