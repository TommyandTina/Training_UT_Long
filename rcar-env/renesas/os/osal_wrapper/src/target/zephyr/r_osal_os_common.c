/* PRQA S 9106 1 # This function is intended to be used in common. */
void* R_OSAL_OS_Malloc(size_t size)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    void *p_ret_value;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    if (0U == size)
    {
        p_ret_value = NULL;
    }
    else
    {
        /* PRQA S 5118 1 # Memory allocation functions are available during initialization. */
        p_ret_value = calloc(1, size);
    }

    return p_ret_value;
}
